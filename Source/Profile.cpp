static bool checkVarCompare(
	const std::string& theVarStr,
	const std::string& theParamStr,
	double theVarNum, double theParamNum,
	char theOperator)
{
	switch(theOperator)
	{
	case '<': return theVarNum < theParamNum;
	case '(': return theVarNum <= theParamNum;
	case '>': return theVarNum > theParamNum;
	case ')': return theVarNum >= theParamNum;
	case '?': return stringToBool(theVarStr);
	case '=':
		if( !_isnan(theVarNum) && !_isnan(theParamNum) )
			return theVarNum == theParamNum;
		return condense(theVarStr) == condense(theParamStr);
	case '!':
		if( !_isnan(theVarNum) && !_isnan(theParamNum) )
			return theVarNum != theParamNum;
		return condense(theVarStr) != condense(theParamStr);
	default:
		DBG_ASSERT(false && "Unhandled conditional operator");
		return false;
	}
}


static std::string varSelectString(
	const std::string& theVarStr,
	const std::string& theTagStr,
	size_t thePos)
{
	const double theVarNum = stringToDouble(theVarStr, true);
	std::string aCaseStr, aValStr, aStr;
	double aCaseNum = -1.0;
	while(thePos < theTagStr.size())
	{
		char anOpC = '=';
		aStr = fetchNextItem(theTagStr, thePos, ":,]}");
		if( theTagStr[thePos] == ':' )
		{
			// Check for an operator at start of case
			int anOpCharCount = 0;
			switch(aStr[0])
			{
			case '~': case '!':
				anOpC = '!';
				// Ignore extra '=' for '!=' or '~='
				if( aStr[++anOpCharCount] == '=' )
					++anOpCharCount;
				break;
			case '<':
				if( aStr[++anOpCharCount] == '>' )
				{// Treat <> as alternate form of '!'
					anOpC = '!';
					++anOpCharCount;
				}
				else if( aStr[anOpCharCount] == '=' )
				{// Use '(' to mean less-than-or-equal (internally only)
					anOpC = '(';
					++anOpCharCount;
				}
				else
				{
					anOpC = '<';
				}
				break;
			case '>':
				if( aStr[++anOpCharCount] == '=' )
				{// Use ')' to mean less-than-or-equal (internally only)
					anOpC = ')';
					++anOpCharCount;
				}
				else
				{
					anOpC = '>';
				}
				break;
			case '=':
				// Ignore extra '='
				if( aStr[++anOpCharCount] == '=' )
					++anOpCharCount;
				break;
			}
			aCaseStr = aStr.substr(anOpCharCount);
			aCaseNum = stringToDouble(aCaseStr, true);
			aValStr = fetchNextItem(theTagStr, ++thePos, ",]}");
			if( (_isnan(aCaseNum) || _isnan(theVarNum)) &&
				anOpC != '!' && anOpC != '=' )
			{
				logError("Variable select case comparison of '%s %s' "
					"expected numeric values for this operator type!",
					theVarStr.c_str(), aStr.c_str());
				// Skip this invalid case, or return it if last one anyway
				if( theTagStr[thePos++] != ',' )
					return aValStr;
				continue;
			}
		}
		else
		{
			// No case found - must be only value was specified
			// Use previous case + 1 as assumed next case if using numbers
			if( !_isnan(aCaseNum) )
				aCaseNum = floor(aCaseNum) + 1.0;
			// Assume this is instead the value string
			aValStr = aStr;
		}

		// If reached end of [] block without returning anything yet, return
		// last value found as the default (even if it wouldn't have matched)
		if( theTagStr[thePos++] != ',' )
			return aValStr;

		// Check if case matches and if so return value found as replacement
		if( checkVarCompare(theVarStr, aCaseStr, theVarNum, aCaseNum, anOpC) )
			return aValStr;
	}

	return aValStr;
}


static std::string varTagToString(
	const std::string& theVarContents,
	const std::string& theTagStr,
	const size_t theFirstOperatorPos,
	VarPropDependency* theVarDep = null,
	bool init = false)
{
	std::string result = theVarContents;
	// Replace quoted string with un-trimmed contents
	if( result.size() >= 2 &&
		((result[0] == '"' && result[result.size()-1] == '"') ||
		 (result[0] == '\'' && result[result.size()-1] == '\'')) )
	{
		result = result.substr(1, result.size()-2);
	}

	DBG_ASSERT(theFirstOperatorPos < theTagStr.size());
	size_t aPos = theFirstOperatorPos;
	// Figure out what operator we're dealing with (some use 2 chars)
	u8 anOpC = theTagStr[aPos++];
	const char* aNextDel = "?}";
	switch(anOpC)
	{
	case '}':
		return result;
	case '?':
		aNextDel = ":}";
		break;
	case '~':
		anOpC = '!';
		// fall through
	case '!':
		// Ignore extra '=' for '!=' or '~='
		if( theTagStr[aPos] == '=' )
			++aPos;
		break;
	case '<':
		if( theTagStr[aPos] == '>' )
		{// Treat <> as alternate form of '!'
			anOpC = '!';
			++aPos;
		}
		else if( theTagStr[aPos] == '=' )
		{// Use '(' to mean less-than-or-equal (internally only)
			anOpC = '(';
			++aPos;
		}
		break;
	case '>':
		if( theTagStr[aPos] == '=' )
		{// Use ')' to mean less-than-or-equal (internally only)
			anOpC = ')';
			++aPos;
		}
		break;
	case '=':
		// Ignore extra '='
		if( theTagStr[aPos] == '=' )
			++aPos;
		break;
	case '/':
		if( theTagStr[aPos] == '~' )
		{// Use /~ to for mod operator
			anOpC = '%';
			++aPos;
		}
		// fall through
	case '+': case '-': case '*':
		aNextDel = "}+*/?[!~<>="; // to warn of multi-operator use (except -)
		break;
	case '.':
		// Not really an operator, so find the actual operator next (if any)
		aNextDel = "}+-*/?[!~<>=.";
		break;
	case '[':
		return varSelectString(theVarContents, theTagStr, aPos);
	default:
		DBG_ASSERT(false && "Unhandled operator character");
	}

	// Get first parameter for operator
	const std::string& aParam = fetchNextItem(theTagStr, aPos, aNextDel);

	// Check for special .anchor, .offset, .x/xs, and .y/ys for coord vars,
	// as well as .sum, .int, .round, .floor, and .ceil for sum vars
	if( anOpC == '.' )
	{
		enum EMemberType {
			eMemberType_Unknown,
			eMemberType_X,
			eMemberType_Y,
			eMemberType_XScaled,
			eMemberType_YScaled,
			eMemberType_Offset,
			eMemberType_Anchor,
			eMemberType_Sum,
			eMemberType_Round,
			eMemberType_Floor,
			eMemberType_Ceil,
			eMemberType_Abs,

			eMemberType_LastCoordType = eMemberType_Anchor
		};
		struct NameToEnumMapper
		{
			typedef StringToValueMap<EMemberType, u8> NameToEnumMap;
			NameToEnumMap map;
			NameToEnumMapper()
			{
				struct { const char* str; EMemberType val; }
				kEntries[] = {
					{ "x",			eMemberType_X		},
					{ "y",			eMemberType_Y		},
					{ "xs",			eMemberType_XScaled	},
					{ "ys",			eMemberType_YScaled	},
					{ "offset",		eMemberType_Offset	},
					{ "anchor",		eMemberType_Anchor	},
					{ "sum",		eMemberType_Sum		},
					{ "int",		eMemberType_Round	},
					{ "integer",	eMemberType_Round	},
					{ "num",		eMemberType_Round	},
					{ "round",		eMemberType_Round	},
					{ "floor",		eMemberType_Floor	},
					{ "ceil",		eMemberType_Ceil	},
					{ "abs",		eMemberType_Abs		},
				};
				map.reserve(ARRAYSIZE(kEntries));
				for(int i = 0; i < ARRAYSIZE(kEntries); ++i)
					map.setValue(kEntries[i].str, kEntries[i].val);
			}
		};
		static NameToEnumMapper sNameToEnumMapper;
		const EMemberType aMemberType =
			sNameToEnumMapper.map.valElse(aParam, eMemberType_Unknown);
		if( aMemberType == eMemberType_Unknown )
		{
			logError("Uknown variable string conversion name '.%s' in '%s'!",
				aParam.c_str(), theTagStr.c_str());
			return varTagToString(result, theTagStr, aPos);
		}
		if( aMemberType < eMemberType_LastCoordType )
		{
			size_t aCoordStrPos = 0;
			const Hotspot::Coord& aCoord = stringToCoord(result, aCoordStrPos);
			if( aCoordStrPos != result.size() )
			{
				logError("Expected an anchor + offset coordinate string "
					"instead of '%s' for conversion type '.%s' in '%s'!",
					result.c_str(), aParam.c_str(), theTagStr.c_str());
				return varTagToString(result, theTagStr, aPos);
			}
			switch(aMemberType)
			{
			case eMemberType_X:
				{
					const u32 aMaxVal = stringToU32(sSectionsMap.vals()
						[kVarsSectionIdx].vals()[kWidthVarID].str);
					const int anAnchorVal =
						u16ToRangeVal(aCoord.anchor, aMaxVal);
					result = toString(clamp(
						anAnchorVal + aCoord.offset, 0, aMaxVal));
					if( theVarDep )
					{// Make sure this recalculates when needed
						theVarDep->varID = kWidthVarID;
						logPropVarDependency(*theVarDep, init);
					}
				}
				break;
			case eMemberType_Y:
				{
					const u32 aMaxVal = stringToU32(sSectionsMap.vals()
						[kVarsSectionIdx].vals()[kHeightVarID].str);
					const int anAnchorVal =
						u16ToRangeVal(aCoord.anchor, aMaxVal);
					result = toString(clamp(
						anAnchorVal + aCoord.offset, 0, aMaxVal));
					if( theVarDep )
					{// Make sure this recalculates when needed
						theVarDep->varID = kHeightVarID;
						logPropVarDependency(*theVarDep, init);
					}
				}
				break;
			case eMemberType_XScaled:
				{
					const u32 aMaxVal = stringToU32(sSectionsMap.vals()
						[kVarsSectionIdx].vals()[kWidthVarID].str);
					const int anAnchorVal =
						u16ToRangeVal(aCoord.anchor, aMaxVal);
					const double aScale = stringToDouble(sSectionsMap.vals()
						[kVarsSectionIdx].vals()[kUIScaleVarID].str);
					result = toString(clamp(
						anAnchorVal + int(aCoord.offset * aScale), 0, aMaxVal));
					if( theVarDep )
					{// Make sure this recalculates when needed
						theVarDep->varID = kWidthVarID;
						logPropVarDependency(*theVarDep, init);
						theVarDep->varID = kUIScaleVarID;
						logPropVarDependency(*theVarDep, init);
					}
				}
				break;
			case eMemberType_YScaled:
				{
					const u32 aMaxVal = stringToU32(sSectionsMap.vals()
						[kVarsSectionIdx].vals()[kHeightVarID].str);
					const int anAnchorVal =
						u16ToRangeVal(aCoord.anchor, aMaxVal);
					const double aScale = stringToDouble(sSectionsMap.vals()
						[kVarsSectionIdx].vals()[kUIScaleVarID].str);
					result = toString(clamp(
						anAnchorVal + int(aCoord.offset * aScale), 0, aMaxVal));
					if( theVarDep )
					{// Make sure this recalculates when needed
						theVarDep->varID = kHeightVarID;
						logPropVarDependency(*theVarDep, init);
						theVarDep->varID = kUIScaleVarID;
						logPropVarDependency(*theVarDep, init);
					}
				}
				break;
			case eMemberType_Offset:
				result = toString(aCoord.offset);
				break;
			case eMemberType_Anchor:
				result = toString(aCoord.anchor / double(0x10000U));
				break;
			}
			return varTagToString(result, theTagStr, aPos);
		}
		else
		{
			size_t aSumStrPos = 0;
			double aSum = stringToDoubleSum(result, aSumStrPos);
			if( aSumStrPos != result.size() )
			{
				logError("Expected string representing a sum of numbers "
					"instead of '%s' for conversion type '.%s' in '%s'!",
					result.c_str(), aParam.c_str(), theTagStr.c_str());
				return varTagToString(result, theTagStr, aPos);
			}

			switch(aMemberType)
			{
			case eMemberType_Sum:
				result = toString(aSum);
				break;
			case eMemberType_Round:
				result = toString(int(floor(aSum+0.5)));
				break;
			case eMemberType_Floor:
				result = toString(int(floor(aSum)));
				break;
			case eMemberType_Ceil:
				result = toString(int(ceil(aSum)));
				break;
			case eMemberType_Abs:
				result = toString(abs(aSum));
				break;
			}
			return varTagToString(result, theTagStr, aPos);
		}
	}

	// Check for valid values (i.e. numbers) for some operators
	double aVarNum = 0, aParamNum = 0;
	if( anOpC != '?' )
	{
		aVarNum = stringToDouble(result, true);
		if( _isnan(aVarNum) && anOpC != '!' && anOpC != '=' )
		{
			if( anOpC == '+' || anOpC == '-' )
			{// Check if a hotspot coord format and can just +/- the offset
				size_t aCoordStrPos = 0;
				stringToCoord(result, aCoordStrPos);
				if( aCoordStrPos == result.size() )
				{// Valid hotspot coord format! Just append operator & param
					result += theTagStr.substr(theFirstOperatorPos, 
						theTagStr.find('}') - theFirstOperatorPos);
					return result;
				}
			}
			logError("Expected a number instead of '%s' for "
				"variable expansion operator in '%s'!",
				result.c_str(), theTagStr.c_str());
			aVarNum = stringToBool(aParam) ? 1.0 : 0;
		}
		aParamNum = stringToDouble(aParam, true);
		if( _isnan(aParamNum) && anOpC != '!' && anOpC != '=' )
		{
			logError("Expected a number instead of '%s' for "
				"variable expansion operator in '%s'. "
				"Note only one operator is allowed per ${} block!",
				aParam.c_str(), theTagStr.c_str());
			aParamNum =
				(anOpC == '+' || anOpC == '-' ) ? 0 :
				(anOpC == '*' || anOpC == '/' || anOpC == '%') ? 1.0 :
				stringToBool(aParam) ? 1.0 : 0;
		}
	}

	// Have all we need for arithmetic operators now
	if( anOpC == '+' || anOpC == '-' ||
		anOpC == '*' || anOpC == '/' || anOpC == '%' )
	{
		if( theTagStr[aPos] != '}' )
		{
			logError("Unexpected %c found in '%s'. "
				"Only one operator is allowed per ${} block! "
				"Use nested ${} blocks for more options.",
				theTagStr[aPos], theTagStr.c_str());
		}
		switch(anOpC)
		{
		case '+': aVarNum += aParamNum; break;
		case '-': aVarNum -= aParamNum; break;
		case '*': aVarNum *= aParamNum; break;
		case '/':
			if( aParamNum == 0 )
			{
				logError("Division by 0 detected in '%s'!",
					theTagStr.c_str());
				aParamNum = 1.0;
			}
			aVarNum /= aParamNum;
			break;
		case '%':
			aParamNum = floor(aParamNum + 0.5);
			if( aParamNum == 0 )
			{
				logError("Division by 0 detected in '%s'!",
					theTagStr.c_str());
				aParamNum = 1.0;
			}
			aVarNum = int(floor(aVarNum + 0.5)) % int(aParamNum);
			break;
		}
		result = toString(aVarNum);
		return result;
	}

	// Math done, should only be comparison or ? operator now
	const bool isTrue = checkVarCompare(
		result, aParam, aVarNum, aParamNum, anOpC);

	// What to do with compare result depends on second operator
	const char anOpC2 = theTagStr[aPos++];
	if( anOpC2 == '}' )
	{
		if( anOpC == '?' )
		{// ${Var ? resultIfTrue}
			result = isTrue ? aParam : "";
			return result;
		}
		// ${Var !<=> value}
		result = isTrue ? "1" : "0";
		return result;
	}

	if( anOpC2 == ':' )
	{// ${Var ? resultIfTrue : resultIfFalse}
		DBG_ASSERT(anOpC == '?');
		if( isTrue )
		{
			result = aParam;
			return result;
		}
		result = fetchNextItem(theTagStr, aPos, "}");
		return result;
	}

	// ${Var !<=> value ? ...} if got this far
	DBG_ASSERT(anOpC2 == '?');

	// Get result for true, or to advance aPos to : for false condition result
	const std::string& aTrueResult = fetchNextItem(theTagStr, aPos, ":}");
	if( isTrue )
		result = aTrueResult;
	else if( theTagStr[aPos++] == ':' )
		result = fetchNextItem(theTagStr, aPos, "}");
	else
		result = "";

	// result is now aTrueResult if condition was true, the section after :
	// if condition was false, or empty if there was no ':' section
	// (${Var !<=> value ? result} format) and condition was false
	return result;
}


static void expandPropertyVars(int theSectionID, int thePropID, bool init)
{
	PropertyMap& theSection = sSectionsMap.vals()[theSectionID];
	Property& theProp = theSection.vals()[thePropID];
	if( !theProp.str.empty() || theProp.pattern.empty() )
		return;

	std::pair<std::string::size_type, std::string::size_type> aTagCoords =
		findStringTag(theProp.pattern, 0, "${", '}');

	if( aTagCoords.first == std::string::npos )
	{// No variables referenced - set .str and clear .pattern via swap
		swap(theProp.str, theProp.pattern);
		return;
	}

	VarPropDependency* aDepPtr = null;
	VarPropDependency aDep;
	aDep.sectID = theSectionID;
	aDep.propID = thePropID;
	PropertyMap& theVarsMap = sSectionsMap.vals()[kVarsSectionIdx];
	DBG_ASSERT(sSectionsMap.keys()[kVarsSectionIdx] == kVariablesSectionName);

	theProp.str = "\b"; // to detect circular reference from recursion
	std::string aStr = theProp.pattern;
	do {
		// Extract variable name (or a number as a const var) from tag
		std::string aRepStr;
		std::string aVarContents;
		const std::string kVarOpChars = "}+-*/?[!~<>=.";
		size_t aVarNameStartPos = aTagCoords.first + 2;
		while(u8(aStr[aVarNameStartPos]) <= ' ')
			++aVarNameStartPos;
		size_t aVarOpPos = aVarNameStartPos;
		bool validVarFound = false;
		if( aStr[aVarNameStartPos] == '-' ||
			aStr[aVarNameStartPos] == '+' ||
			aStr[aVarNameStartPos] == '.' ||
			(aStr[aVarNameStartPos] >= '0' && aStr[aVarNameStartPos] <= '9') )
		{// Possibly a numeric constant in place of an actual variable name
			const double aVarAsNum = stringToDoubleSum(aStr, aVarOpPos);
			if( aVarOpPos > aVarNameStartPos &&
				kVarOpChars.find(aStr[aVarOpPos]) != std::string::npos )
			{
				aVarContents = toString(aVarAsNum);
				validVarFound = true;
			}
		}
		else
		{
			const std::string& aVarName =
				fetchNextItem(aStr, aVarOpPos, kVarOpChars.c_str());
			const int aVarID = theVarsMap.findIndex(aVarName);
			if( aVarID < theVarsMap.size() )
			{
				Property& aVarProp = theVarsMap.vals()[aVarID];
				if( aVarProp.str == "\b" )
				{
					logError("Variable '%s' references self!",
						aVarName.c_str());
				}
				else
				{
					aDepPtr = &aDep;
					aDep.varID = aVarID;
					logPropVarDependency(aDep, init);
					if( aVarProp.str.empty() && !aVarProp.pattern.empty() )
						expandPropertyVars(kVarsSectionIdx, aVarID, init);
					aVarContents = aVarProp.str;
					validVarFound = true;
				}
			}
		}

		DBG_ASSERT(aVarOpPos < aTagCoords.first + aTagCoords.second);
		if( !validVarFound )
		{
			logError("Could not identify variable name from %s in property "
				"[%s] / %s!",
				aStr.substr(aTagCoords.first, aTagCoords.second).c_str(),
				sSectionsMap.keys()[theSectionID].c_str(),
				theSection.keys()[thePropID].c_str());
			expandPropertyVars(theSectionID, thePropID, init);
		}
		else
		{
			aRepStr = varTagToString(
				aVarContents, aStr, aVarOpPos, aDepPtr, init);
		}
		aStr = aStr.replace(aTagCoords.first, aTagCoords.second, aRepStr);
		aTagCoords = findStringTag(aStr, 0, "${", '}');
	} while( aTagCoords.first != std::string::npos );

	theProp.str = aStr;
}


static void readProfileCallback(
	const std::string& theSection,
	const std::string& theName,
	const std::string& theValue,
	void*)
{
	int aSectionID = sSectionsMap.findOrAddIndex(theSection);
	PropertyMap& aSection = sSectionsMap.vals()[aSectionID];
	if( aSectionID == kVarsSectionIdx &&
		(((theName[0] < 'A' || theName[0] > 'Z') &&
		  (theName[0] < 'a' || theName[0] > 'z')) ||
		 (theName.find_first_of("{}+-*/?[:!~<>=.") != std::string::npos)) )
	{
		logError(
			"Variable names must only contain alphanumeric characters "
			"and first character must be A-Z. '%s' is invalid.",
			theName.c_str());
		return;
	}
	Property& aProperty = aSection.findOrAdd(theName);
	aProperty.pattern = theValue;
	aProperty.str.clear();
}


static void propogatePropertyChange(int theSectionID, int thePropertyID)
{
	// Apply variable expansion to this property
	DBG_ASSERT(theSectionID >= 0 && theSectionID < sSectionsMap.size());
	PropertyMap& aSection = sSectionsMap.vals()[theSectionID];
	DBG_ASSERT(thePropertyID >= 0 && thePropertyID < aSection.size());
	Property& aProp = aSection.vals()[thePropertyID];
	if( !aProp.pattern.empty() )
	{
		aProp.str.clear();
		expandPropertyVars(theSectionID, thePropertyID, false);
	}

	if( theSectionID == kVarsSectionIdx )
	{// Property is a variable - update other properties that depend on it
		VarPropDependency aSearchVarDep;
		aSearchVarDep.varID = thePropertyID;
		for(std::vector<VarPropDependency>::const_iterator itr =
				std::lower_bound(
					sVarPropDepList.begin(),
					sVarPropDepList.end(),
					aSearchVarDep);
			itr != sVarPropDepList.end() && int(itr->varID) == thePropertyID;
			++itr)
		{
			propogatePropertyChange(itr->sectID, itr->propID);
		}
	}
	else
	{// Log property update in sChangedSectionsMap for other modules as well
		sChangedSectionsMap.findOrAdd(sSectionsMap.keys()[theSectionID])
			.setValue(aSection.keys()[thePropertyID], aProp);
	}
}


static void setPropertyAfterLoad(
	int theSectionID,
	int thePropertyID,
	const std::string& theValue,
	bool saveToFile)
{
	DBG_ASSERT(theSectionID >= 0 && theSectionID < sSectionsMap.size());

	PropertyMap& aSection = sSectionsMap.vals()[theSectionID];
	DBG_ASSERT(thePropertyID >= 0 && thePropertyID < aSection.size());
	Property& aProp = aSection.vals()[thePropertyID];

	const bool saveToMap = aProp.pattern.empty()
		? (aProp.str != theValue) : (aProp.pattern != theValue);
	saveToFile = saveToFile &&
		(aProp.file.empty() ? saveToMap : aProp.file != theValue);

	// If a temporary change, make a backup of original file string so that
	// if try re-applying the same value later but with saveToFile as true,
	// can detect need to save even though active map won't be updated.
	// Alternatively, if have a backup and below will set active value back
	// to matching what is saved to file, then no longer need the backup.
	if( saveToMap && !saveToFile )
	{
		if( aProp.file.empty() )
			swap(aProp.file, aProp.pattern.empty() ? aProp.str : aProp.pattern);
		else if( aProp.file == theValue )
			aProp.file.clear();
	}

	if( saveToMap )
	{
		// Set pattern string (at least temporarily - below may clear it)
		aProp.pattern = theValue;

		// Apply variable expansion (or just move from .pattern to .str),
		// if a variable property propogate it to other properties,
		// and log in changes map so outside modules are informed of change
		propogatePropertyChange(theSectionID, thePropertyID);
	}

	if( saveToFile )
	{// Log as change to save to file
		for(int i = 0, end = intSize(sUnsavedChangesList.size()); i < end; ++i)
		{
			if( sUnsavedChangesList[i].sectionID == theSectionID &&
				sUnsavedChangesList[i].propertyID == thePropertyID )
			{
				sUnsavedChangesList[i].value = theValue;
				return;
			}
		}

		sUnsavedChangesList.push_back(UnsavedProperty());
		sUnsavedChangesList.back().sectionID = theSectionID;
		sUnsavedChangesList.back().propertyID = thePropertyID;
		sUnsavedChangesList.back().value = theValue;

		// File data will now match active map data, so can set .file to be
		// the same as .pattern or .str, but .file.empty() means the same thing
		aProp.file.clear();
	}
}


static void linkParentProfiles()
{
	// NOTE: sProfiles.size() can grow during this, so must be checked each loop
	for(int i = 1; i < intSize(sProfiles.size()); ++i)
	{
		// Make a copy in case sProfiles gets invalidated during callback
		const std::string aPath = sProfiles.vals()[i].path;
		if( fileExists(aPath) )
			parseINI(addParentCallback, aPath, eParseMode_Header, &i);
	}
}


static void syncProfileDisplayList()
{
	// Clear any previous parent data
	for(int i = 1, end = sProfiles.size(); i < end; ++i)
	{
		sProfiles.vals()[i].isAParent = false;
		sProfiles.vals()[i].parent.clear();
	}

	// Scan folder for all profile .ini files
	WIN32_FIND_DATA aFindData;
	const std::wstring& aSearchPattern =
		widen(iniFolderPath()) +
		widen(kProfileFileNamePrefix) + L'*' +
		widen(kProfileFileNameSuffix);
	HANDLE hFind = FindFirstFile(aSearchPattern.c_str(), &aFindData);
	if( hFind != INVALID_HANDLE_VALUE )
	{
		do {
			if( !(aFindData.dwFileAttributes & FILE_ATTRIBUTE_DIRECTORY) )
			{
				const std::string& aFileName = narrow(aFindData.cFileName);
				const int aProfileID =
					sProfiles.findOrAddIndex(extractProfileName(aFileName));
				if( aProfileID == 0 )
					continue;
				sProfiles.vals()[aProfileID].path = iniFolderPath() + aFileName;
			}
		} while(FindNextFile(hFind, &aFindData));
	}
	FindClose(hFind);
	sPerformedProfileFolderScan = true;

	// Link profiles to parents (may generate new parents!)
	linkParentProfiles();

	// Generate a new list but attempting to maintain previous list order by
	// first adding any valid entries from the old list to a StringToValueMap,
	// which stores keys in order added but eliminates duplicates.
	StringToValueMap<int> aNewDisplayList;
	aNewDisplayList.reserve(intSize(sDisplayedProfileList.size()));
	for(int i = 0, end = intSize(sDisplayedProfileList.size()); i < end; ++i)
	{
		if( sDisplayedProfileList[i].empty() )
			continue;
		if( ProfileData* aProfile = sProfiles.find(sDisplayedProfileList[i]) )
		{
			if( !aProfile->isAParent && fileExists(aProfile->path) )
				aNewDisplayList.findOrAdd(sDisplayedProfileList[i]);
		}
	}
	// Now add any valid entries that are missing from the old list
	for(int i = 1, end = intSize(sProfiles.size()); i < end; ++i)
	{
		if( sProfiles.vals()[i].isAParent )
			continue;
		if( !fileExists(sProfiles.vals()[i].path) )
			continue;
		aNewDisplayList.findOrAdd(sProfiles.keys()[i]);
	}

	// Transfer aNewDisplayList.keys() to sDisplayedProfileList, recording any
	// that are different in Core .ini file as go through them.
	// Do not shrink displayed list yet so can write out blank entries to Core,
	// but make sure it is at least as large as new list is now.
	if( sDisplayedProfileList.size() < size_t(aNewDisplayList.size()) )
		sDisplayedProfileList.resize(aNewDisplayList.size());
	for(int i = 0, end = intSize(sDisplayedProfileList.size()); i < end; ++i)
	{
		const std::string& aProfileName =
			i < aNewDisplayList.size()
				? aNewDisplayList.keys()[i]
				: std::string();
		if( sDisplayedProfileList[i] != aProfileName )
		{
			sDisplayedProfileList[i] = aProfileName;
			setPropertyInINI(
				sProfiles.vals()[0].path, "",
				std::string(kProfileKeyPrefix) + toString(i+1),
				aProfileName);
		}
	}
	sDisplayedProfileList.resize(aNewDisplayList.size());
}


static void loadCoreProfile()
{
	// Start with a fresh map
	sSectionsMap.clear();
	confirmSafeToWriteToMap();
	// Make sure "variables" section is always section 0
	sSectionsMap.setValue(kVariablesSectionName, PropertyMap());
	sVarPropDepList.clear();
	// Add built-in system variables to variables section
	PropertyMap& aVarMap = sSectionsMap.vals()[kVarsSectionIdx];
	Property aProp;
	aProp.str = "1.0"; aVarMap.setValue(kUIScaleVarName, aProp);
	aProp.str = "1920"; aVarMap.setValue(kWidthVarName, aProp);
	aProp.str = "1080"; aVarMap.setValue(kHeightVarName, aProp);
	aProp.str = "false"; aVarMap.setValue(kLinuxVarName, aProp);
	aProp.str = "Yes"; aVarMap.setValue(kCopyIconsVarName, aProp);
	aProp.str = "Yes"; aVarMap.setValue(kCopyExcludeOverlaysVarName, aProp);
	aProp.str = ".\\"; aVarMap.setValue("TargetAppDir", aProp);
	aProp.str = toString(GetSystemMetrics(SM_CXSCREEN));
	aVarMap.setValue("LastGameWindowWidth", aProp);
	aProp.str = toString(GetSystemMetrics(SM_CYSCREEN));
	aVarMap.setValue("LastGameWindowHeight", aProp);
	DBG_ASSERT(sSectionsMap.keys()[kVarsSectionIdx] == kVariablesSectionName);
	DBG_ASSERT(aVarMap.keys()[kUIScaleVarID] == kUIScaleVarName);
	DBG_ASSERT(aVarMap.keys()[kWidthVarID] == kWidthVarName);
	DBG_ASSERT(aVarMap.keys()[kHeightVarID] == kHeightVarName);

	DBG_ASSERT(!sProfiles.empty());
	if( !fileExists(sProfiles.vals()[0].path) )
	{
		logFatalError(
			"Could not find required Core profile %s",
			sProfiles.vals()[0].path.c_str());
		return;
	}
	checkForOutdatedFileVersion(sProfiles.vals()[0].path);
	parseINI(
		readProfileCallback,
		sProfiles.vals()[0].path,
		eParseMode_Sections);
}


static void loadProfileWithParentFirst(const ProfileData& theProfile)
{
	if( !theProfile.parent.empty() )
	{
		loadProfileWithParentFirst(sProfiles.quickFind(theProfile.parent));
		if( hadFatalError() )
			return;
	}

	if( !fileExists(theProfile.path) )
	{
		Dialogs::showError(strFormat(
			"Could not find expected profile file: %s",
			theProfile.path.c_str()));
		return;
	}

	checkForOutdatedFileVersion(theProfile.path);
	parseINI(
		readProfileCallback,
		theProfile.path,
		eParseMode_Sections);
}


static int queryUserForProfileImpl(int& theLastSelectedProfileIdx)
{
	int result = 0;
	DBG_ASSERT(!sProfiles.empty());
	if( hadFatalError() )
		return result;

	// Create lists needed for dialog
	enum EType
	{
		eType_CopyFile,			// index into sKnownFiles
		eType_CopyResCustom,	// index into kResTemplateCustom
		eType_ParentResBase,	// index into kResTemplateBase
		eType_ParentResDefault,	// index into kResTemplateDefault
		eType_ParentFile,		// index into sKnownFiles
	};
	std::vector<std::string> aTemplateProfileNames;
	std::vector<int> aTemplateRelativeIndexes;
	std::vector<EType> aTemplateProfileType;
	const std::string kCopyFilePrefix = "Copy \"";
	const std::string kParentPrefix = "Customize \"";

	for(int i = 0, end = intSize(sDisplayedProfileList.size()); i < end; ++i)
	{
		aTemplateProfileNames.push_back(
			kCopyFilePrefix + sDisplayedProfileList[i] + '"');
		aTemplateRelativeIndexes.push_back(i);
		aTemplateProfileType.push_back(eType_CopyFile);
	}

	const bool needFirstProfile = sDisplayedProfileList.empty();
	for(int i = 0; i < ARRAYSIZE(kResTemplateCustom); ++i)
	{
		if( needFirstProfile )
			sDisplayedProfileList.push_back(kResTemplateCustom[i].dispName);
		aTemplateProfileNames.push_back(
			kCopyFilePrefix + kResTemplateCustom[i].dispName + '"');
		aTemplateRelativeIndexes.push_back(i);
		aTemplateProfileType.push_back(eType_CopyResCustom);
	}

	for(int i = 0, end = intSize(sProfiles.size()); i < end; ++i)
	{
		aTemplateProfileNames.push_back(
			kParentPrefix + sProfiles.keys()[i] + '"');
		aTemplateRelativeIndexes.push_back(i);
		aTemplateProfileType.push_back(eType_ParentFile);
	}

	for(int i = 0; i < ARRAYSIZE(kResTemplateBase); ++i)
	{
		if( sProfiles.contains(kResTemplateBase[i].fileName) )
			continue;
		aTemplateProfileNames.push_back(
			kParentPrefix + kResTemplateBase[i].dispName + '"');
		aTemplateRelativeIndexes.push_back(i);
		aTemplateProfileType.push_back(eType_ParentResBase);
	}

	for(int i = 0; i < ARRAYSIZE(kResTemplateDefault); ++i)
	{
		if( sProfiles.contains(kResTemplateBase[i].fileName) )
			continue;
		aTemplateProfileNames.push_back(
			kParentPrefix + kResTemplateDefault[i].dispName + '"');
		aTemplateRelativeIndexes.push_back(i);
		aTemplateProfileType.push_back(eType_ParentResDefault);
	}
	DBG_ASSERT(aTemplateRelativeIndexes.size() == aTemplateProfileNames.size());
	DBG_ASSERT(aTemplateProfileType.size() == aTemplateProfileNames.size());

	// Use Dialog to ask the user what they want to do
	int aLoadedProfileDisplayIdx = -1;
	if( sLoadedProfileIdx > 0 )
	{
		const std::string& aLoadedProfileName =
			sProfiles.keys()[sLoadedProfileIdx];
		for(int i = 0, end = intSize(sDisplayedProfileList.size());
			i < end; ++i)
		{
			if( sDisplayedProfileList[i] == aLoadedProfileName )
				aLoadedProfileDisplayIdx = i;
		}
	}
	int aDefaultSelectedIdx =
		theLastSelectedProfileIdx >= 0
			? theLastSelectedProfileIdx
			: aLoadedProfileDisplayIdx;
	Dialogs::ProfileSelectResult aDialogResult = Dialogs::profileSelect(
		sDisplayedProfileList,
		aTemplateProfileNames,
		aDefaultSelectedIdx,
		aLoadedProfileDisplayIdx,
		sAutoLoadProfileIdx > 0,
		needFirstProfile);

	// Regardless of whatever else chose, if un-checked AutoLoad then remember
	if( !aDialogResult.autoLoadRequested )
		setAutoLoadProfile(0);

	// Regardless of whatever else chose, save changes to display list order
	if( aDialogResult.orderChanged )
	{
		for(int i = 0, end = intSize(sDisplayedProfileList.size());
			i < end; ++i)
		{
			setPropertyInINI(
				sProfiles.vals()[0].path, "",
				std::string(kProfileKeyPrefix) + toString(i+1),
				sDisplayedProfileList[i]);
		}
	}

	if( aDialogResult.cancelled )
	{// User declined to select anything
		return result; // 0
	}

	// For first profile, if no name given then use built-in name
	// NOTE: This only works out because set the first entries of
	// aTemplateProfile's to match aLoadableProfile's which both
	// match the entries in kResTemplateCustom. So need to change
	// this if rearrange the order of any of these above!
	if( needFirstProfile && aDialogResult.newName.empty() )
	{
		aDialogResult.newName =
			kResTemplateCustom[aDialogResult.selectedIndex].fileName;
	}

	if( aDialogResult.newName.empty() )
	{// User must have requested to load/edit/delete an existing profile
		DBG_ASSERT(size_t(aDialogResult.selectedIndex) <
			sDisplayedProfileList.size());
		theLastSelectedProfileIdx = aDialogResult.selectedIndex;
		result = sProfiles.quickFindIndex(
			sDisplayedProfileList[aDialogResult.selectedIndex]);
		if( aDialogResult.editProfileRequested )
		{
			userEditProfile(result, false);
			syncProfileDisplayList();
			result = -1; // repeat query from the start
		}
		else if( aDialogResult.deleteProfileRequested )
		{
			const std::string& aProfName =
				sDisplayedProfileList[aDialogResult.selectedIndex];
			if( ProfileData* aProfile = sProfiles.find(aProfName) )
			{
				if( Dialogs::yesNoPrompt(strFormat(
						"Are you sure you wish to delete '%s'?\n\n"
						"NOTE: This will NOT automatically delete any parent " 
						"profiles of '%s'.",
						aProfile->path.c_str(),
						aProfName.c_str()),
						"Delete Profile") == eResult_Yes )
				{
					DeleteFile(widen(aProfile->path.c_str()).c_str());
					theLastSelectedProfileIdx = -1;
				}
			}
			syncProfileDisplayList();
			result = -1; // repeat query from the start
		}
		else if( aDialogResult.autoLoadRequested )
		{
			setAutoLoadProfile(result);
		}
		return result;
	}

	// User must have requested creating a new profile
	const std::string& aNewName = extractProfileName(aDialogResult.newName);
	const std::string& aNewPath = profileNameToFilePath(aNewName);

	if( fileExists(aNewPath) )
	{// File already exists
		if( isSamePath(aNewPath, sProfiles.vals()[0].path) )
		{
			Dialogs::showError(strFormat(
				"Can not create custom Profile with the name %s!",
				sProfiles.keys()[0].c_str()));
			result = -1; // repeat query from the start
			return result;
		}
		int aDupProfileIdx = sProfiles.findIndex(aNewName);
		if( aDupProfileIdx >= sProfiles.size() ||
			sProfiles.vals()[aDupProfileIdx].isAParent )
		{
			Dialogs::showError(strFormat(
				"File '%s' already exists and does not allow overwrite "
				"(likely acting as parent to another profile).",
				getFileName(aNewPath).c_str()));
			result = -1; // repeat query from the start
			return result;
		}
		if( Dialogs::yesNoPrompt(strFormat(
				"Profile '%s' already exists. Overwrite?",
					aNewName.c_str()),
				"Overwrite existing Profile") != eResult_Yes )
		{
			result = -1; // repeat query from the start
			return result;
		}
		sProfiles.vals()[aDupProfileIdx].parent.clear();
	}

	int aSrcIdx = aDialogResult.selectedIndex;
	DBG_ASSERT(size_t(aSrcIdx) < aTemplateProfileType.size());
	EType aProfileType = aTemplateProfileType[aDialogResult.selectedIndex];
	if( aProfileType == eType_ParentResDefault ||
		aProfileType == eType_CopyResCustom )
	{// Do not allow Base profile names for a new profile of these types
		// This is to prevent possibly having self as grandparent
		const std::string& aCompName = condense(aNewName);
		for(int i = 0; i < ARRAYSIZE(kResTemplateBase); ++i)
		{
			if( aCompName == condense(kResTemplateBase[i].fileName) )
			{
				Dialogs::showError(
					"Selected Profile name not valid (reserved)!");
				result = -1; // repeat query from the start
				return result;
			}
		}
	}

	if( aProfileType == eType_CopyResCustom )
	{// Do not allow Default profile names for a new profile of these types
		// This is to prevent possibly having self as a parent
		const std::string& aCompName = condense(aNewName);
		for(int i = 0; i < ARRAYSIZE(kResTemplateDefault); ++i)
		{
			if( aCompName == condense(kResTemplateDefault[i].fileName) )
			{
				Dialogs::showError(
					"Selected Profile name not valid (reserved)!");
				result = -1; // repeat query from the start
				return result;
			}
		}
	}

	// Add new entry to sProfiles and use index as possible return value
	result = sProfiles.findOrAddIndex(aNewName);
	sProfiles.vals()[result].path = aNewPath;
	DBG_ASSERT(result > 0);

	switch(aProfileType)
	{
	case eType_CopyFile:
		{// Create copy of source file
			const ProfileData& aSrcProfile = sProfiles.quickFind(
				sDisplayedProfileList[aTemplateRelativeIndexes[aSrcIdx]]);
			// Don't actually do anything if source and dest are the same file
			if( !isSamePath(aNewPath, aSrcProfile.path) )
			{
				CopyFile(
					widen(aSrcProfile.path).c_str(),
					widen(aNewPath).c_str(),
					FALSE);
			}
		}
		break;
	case eType_CopyResCustom:
		{// Generate resource profile but with new file name
			const int aResTemplateIdx = aTemplateRelativeIndexes[aSrcIdx];
			DBG_ASSERT(aResTemplateIdx >= 0);
			DBG_ASSERT(aResTemplateIdx < ARRAYSIZE(kResTemplateCustom));
			ResourceFile aResFile = kResTemplateCustom[aResTemplateIdx];
			aResFile.fileName = aNewName.c_str();
			generateResourceFile(aResFile);
		}
		break;
	case eType_ParentResBase:
	case eType_ParentResDefault:
		{// Generate resource profile to act as parent
			const int aResBaseIdx = aTemplateRelativeIndexes[aSrcIdx];
			const char* aProfileName = null;
			if( aProfileType == eType_ParentResBase )
			{
				DBG_ASSERT(aResBaseIdx < ARRAYSIZE(kResTemplateBase));
				generateResourceFile(kResTemplateBase[aResBaseIdx]);
				aProfileName = kResTemplateBase[aResBaseIdx].fileName;
			}
			else
			{
				DBG_ASSERT(aResBaseIdx < ARRAYSIZE(kResTemplateDefault));
				generateResourceFile(kResTemplateDefault[aResBaseIdx]);
				aProfileName = kResTemplateDefault[aResBaseIdx].fileName;
			}
			const int aProfileID = sProfiles.findOrAddIndex(aProfileName);
			sProfiles.vals()[aProfileID].path =
				profileNameToFilePath(aProfileName);
			if( aProfileType == eType_ParentResBase )
				sNewBaseProfileIdx = aProfileID;
			// Treat as _ParentFile type now that resource exists as a file
			aTemplateProfileNames.push_back("");
			aTemplateRelativeIndexes.push_back(aProfileID);
			aTemplateProfileType.push_back(eType_ParentFile);
			aSrcIdx = intSize(aTemplateProfileType.size()) - 1;
		}
		// fall through
	case eType_ParentFile:
		{// Create new file that sets source file as its parent profile
			const int aParentProfileIdx = aTemplateRelativeIndexes[aSrcIdx];
			DBG_ASSERT(aParentProfileIdx < sProfiles.size());
			// Make sure parent and new file are not the same file
			if( isSamePath(sProfiles.vals()[aParentProfileIdx].path, aNewPath) )
			{
				Dialogs::showError(
					"Selected Profile name matches parent profile name!");
				result = -1; // repeat query from the start
				return result;
			}
			ResourceFile aResFile = kResTemplateBlank;
			aResFile.fileName = aNewName.c_str();
			generateResourceFile(aResFile);
			// Specify parent profile (unless it is Core/0)
			if( aParentProfileIdx > 0 )
			{
				setPropertyInINI(
					aNewPath, "",
					"ParentProfile", sProfiles.keys()[aParentProfileIdx]);
			}
		}
		break;
	}

	if( !fileExists(aNewPath) )
	{
		Dialogs::showError(strFormat(
			"Failure creating new Profile '%s' (%s)!",
			aNewName.c_str(), aNewPath.c_str()));
		result = -1; // repeat query from the start
		return result;
	}

	// Add new profile to display list
	if( needFirstProfile )
		sDisplayedProfileList.clear();
	syncProfileDisplayList();
	theLastSelectedProfileIdx = intSize(sDisplayedProfileList.size())-1;

	// Extra prompts for first time create a "base" profile, which represents
	// the shared default values for all profiles for a particular game, and
	// thus need to set some of these default but game-specific settings
	if( sNewBaseProfileIdx >= 0 )
	{
		// Make sure core has been loaded to get needed variable values
		// (such as alternate suggested launch params in Linux)
		if( sSectionsMap.empty() )
			loadCoreProfile();

		// Load defaults values from generated file
		const BaseGameSettings& aDefaultSettings =
			getBaseGameSettings(sProfiles.vals()[sNewBaseProfileIdx].path);
		BaseGameSettings aSettings = aDefaultSettings;

		// Prompt user for auto-launch configuration
		aSettings.autoLaunchAppPath =
			expandVars(aSettings.autoLaunchAppPath);
		aSettings.autoLaunchAppParams =
			expandVars(aSettings.autoLaunchAppParams);
		aSettings.autoQuitWithTargetApp =
			expandVars(aSettings.autoQuitWithTargetApp);
		// Don't ask about auto-launching an app when already have one active
		if( TargetApp::targetAppActive() )
		{
			aSettings.autoLaunchAppPath.clear();
		}
		else if( Dialogs::yesNoPrompt(
				"Would you like to automatically launch target game's "
				"launcher/patcher when loading this profile at startup?",
				"Auto-Launch Target App") == eResult_Yes )
		{
			Dialogs::targetAppPath(
				aSettings.autoLaunchAppPath,
				aSettings.autoLaunchAppParams,
				aSettings.autoQuitWithTargetApp);
		}
		else
		{
			aSettings.autoLaunchAppPath.clear();
		}

		writeBaseGameSettingsChanges(
			sProfiles.vals()[sNewBaseProfileIdx].path,
			aDefaultSettings,
			aSettings);

		// Load read-in known issues key now, before rest of profile,
		// so can generate it and display it immediately
		generateKnownIssuesRTF(aDefaultSettings.knownIssuesList);
		if( sNeedShowKnownIssues )
		{
			Dialogs::showKnownIssues();
			sNeedShowKnownIssues = false;
		}
	}
	sNewBaseProfileIdx = -1;

	if( needFirstProfile )
	{
		if( Dialogs::yesNoPrompt(
				"Would you like to edit this Profile now to match your in-game "
				"Key Binds and other settings?",
				"Edit new profile") == eResult_Yes )
		{
			userEditProfile(result, true);
		}
		else
		{
			return result;
		}
	}

	// Query again to actually load the new profile
	result = -1;
	return result;
}


static int queryUserForProfileID()
{
	saveChangesToFile();
	if( !sPerformedProfileFolderScan )
		syncProfileDisplayList();
	int aLastCreatedProfileIdx = -1;
	int result = -1;
	while(result < 0)
		result = queryUserForProfileImpl(aLastCreatedProfileIdx);
	return result;
}


